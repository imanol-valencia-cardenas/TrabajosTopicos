    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        
        Document documento = new Document();
        
        try{
            String ruta = System.getProperty("user.home");
            PdfWriter.getInstance(documento, new FileOutputStream(ruta + "/Downloads/Inventario_Pasteles.pdf"));
            documento.open();
            
            PdfPTable tabla = new PdfPTable(5);
            tabla.addCell("ID");
            tabla.addCell("Sabor");
            tabla.addCell("Unidades");
            tabla.addCell("Peso(gr)");
            tabla.addCell("Precio($)");
            try{
                Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/bd_pasteleria", "root", "");
                PreparedStatement pst = cn.prepareStatement("select * from pasteles");
                ResultSet rs = pst.executeQuery();
            
                if(rs.next()){
                    do{
                        tabla.addCell(rs.getString(1));
                        tabla.addCell(rs.getString(2));
                        tabla.addCell(rs.getString(3));
                        tabla.addCell(rs.getString(4));
                        tabla.addCell(rs.getString(5));
                    }while (rs.next());
                    documento.add(tabla);
                }
            }catch (DocumentException | SQLException e){
                
            }
            documento.close();
            JOptionPane.showMessageDialog(null, "El Documento ha sido creado");
        }catch (DocumentException | HeadlessException | FileNotFoundException e){
            
        }
        
    }//GEN-LAST:event_jButton5ActionPerformed

