    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        
        try{
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/bd_pasteleria", "root", "");
            PreparedStatement pst = cn.prepareStatement("select * from pasteles where ID = ?");
            pst.setString(1, txt_buscar.getText().trim());
            
            ResultSet rs = pst.executeQuery();
            
            if(rs.next()){
                txt_sabor.setText(rs.getString("Sabor"));
                txt_unidades.setText(rs.getString("Unidades"));
                txt_peso.setText(rs.getString("Peso"));
                txt_precio.setText(rs.getString("Precio"));
            }
            else{
                JOptionPane.showMessageDialog(null, "Este pastel no ha sido registrado");
            }
        }catch (Exception e){
        
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

